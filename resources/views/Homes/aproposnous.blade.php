  <div id="apropo"><p class="text-justify offset-xl-1 col-xl-10">L'idée de créer get2gether.com m'est venue en gérant l'Équipe Les Fées, une association de sportives aventurières que j'avais fondé à la suite de ma participation au Rallye des Gazelles en 1999. Les Fées, cela consistait à nous préparer (logistique et entrainement) ensemble et à partager nos ressources (retombées médiatiques, fournisseurs, commanditaires). Le but était de vivre la solidarité féminine à travers un accomplissement. Nous avons ainsi couru les rallyes Roses des Sables et Les Gazelles, des raids de vélo en Thaïlande et au Kenya, le Grand Tour de Vélo-Québec. Nous avons aussi gravi le Kilimandjaro et pris part à des courses de bateaux-dragons. Au bout de 5 années, plus de 500 femmes avaient relevé l'un des 30 défis des Fées. Mais il en fallait plus. Des quatre coins de la province, on me proposait des activités à ajouter à notre calendrier et je ne pouvais répondre à la demande. Alors j'ai imaginé un site web qui favoriserait un maximum de participations sportives au Québec et à l'étranger, sans que j'aie à les coordonner. Ce site a donc d'abord été prévu pour les femmes. Puis j'ai réalisé qu'il serait plus intéressant s'il était mixte, permettant ainsi de mettre en contact plus de gens ayant en commun l'amour du sport, du plein air, de l'aventure et du défi.

Lancé au printemps 2002, le site s'est d'abord appelé Elfetc.com en référence à ses origines (elf étant l'abréviation d'Équipe Les Fées et etc. pour l'ajout des hommes à la communauté). Mais ce nom s'est avéré difficile à communiquer et trois ans plus tard, nous sommes devenus get2gether.com.

Un réseau unique en son genre

La mission de get2gether.com est d'offrir un lieu d'échanges entre adeptes de sports et de plein air. La condition physique et l'activité sportive sont au premier plan sur le formulaire de création de profil et le module de recherche de membres. Mais puisque notre quotidien ne se limite pas aux rivières que l'on traverse et aux montagnes que l'on gravit, beaucoup d'espace est réservé à la mise en valeur des autres passions de cette communauté au style de vie compatible : voyages, livres, films, musiques, etc. On s'assure ainsi d'un maximum d'affinités communes avant d'établir un premier contact.

Get2gether.com est sans doute le plus grand réseau ciblé québécois et c'est le premier à s'adresser explicitement à la clientèle sportive. C'est aussi celui qui offre le meilleur ratio hommes / femmes. Certains de nos membres s'y proposent des rencontres amicales, plusieurs y trouvent un(e) complice de vie.

Depuis 2002, des milliers de couples se sont formés, des bébés sont nés, des familles se sont reconstituées et, aussi, des milliers de sportifs ont pratiqué ensemble des activités et de belles amitiés sont nées. Quel que soit le but que vous vous fixez, je vous souhaite de vous lancer, de conquérir, de vous accomplir et d'exulter, d'aventures à l'Aventure.

Pour vivre un défi ou vous offrir une nouvelle vie, devenez membre, c'est gratuit !</p>
